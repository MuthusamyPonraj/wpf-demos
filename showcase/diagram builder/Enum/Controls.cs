namespace DiagramBuilder
{
    /// <summary>
    ///     Specifies the different types of controls.
    /// </summary>
    public enum Controls
    {
        /// <summary>
        ///     Specifies the button.
        /// </summary>
        Button,

        /// <summary>
        ///     Specifies the toggle button.
        /// </summary>
        ToggleButton,

        /// <summary>
        ///     Specifies the font family.
        /// </summary>
        FontFamily,

        /// <summary>
        ///     Specifies the font size.
        /// </summary>
        FontSize,

        /// <summary>
        ///     Specifies the combobox.
        /// </summary>
        Combobox,

        /// <summary>
        ///     Specifies the color picker.
        /// </summary>
        ColorPicker,

        /// <summary>
        ///     Specifies the split button.
        /// </summary>
        SplitButton,

        /// <summary>
        ///     Specifies the check box.
        /// </summary>
        CheckBox,

        /// <summary>
        ///     Specifies the gallery.
        /// </summary>
        Gallery
    }
}