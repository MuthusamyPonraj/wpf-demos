namespace DiagramBuilder
{
    /// <summary>
    ///     Specifies the different page size formats.
    /// </summary>
    public enum PageSize
    {
        /// <summary>
        ///     To change the pagesize to letter size format.
        /// </summary>
        Letter,

        /// <summary>
        ///     To change the pagesize to folio size format.
        /// </summary>
        Folio,

        /// <summary>
        ///     To change the pagesize to legal size format.
        /// </summary>
        Legal,

        /// <summary>
        ///     To change the pagesize to ledger size format.
        /// </summary>
        Ledger,

        /// <summary>
        ///     To change the pagesize to a5 size format.
        /// </summary>
        A5,

        /// <summary>
        ///     To change the pagesize to a4 size format.
        /// </summary>
        A4,

        /// <summary>
        ///     To change the pagesize to a3 size format.
        /// </summary>
        A3,

        /// <summary>
        ///     To change the pagesize to a2 size format.
        /// </summary>
        A2,

        /// <summary>
        ///     To change the pagesize to a1 size format.
        /// </summary>
        A1,

        /// <summary>
        ///     To change the pagesize to a0 size format.
        /// </summary>
        A0,

        /// <summary>
        ///     To change the pagesize to custom size.
        /// </summary>
        Custom
    }
}