namespace DiagramBuilder
{
    /// <summary>
    ///     Specifies the certain types of tabs.
    /// </summary>
    public enum Tabs
    {
        /// <summary>
        ///     Enables or disables the home tab.
        /// </summary>
        HOME,

        /// <summary>
        ///     Enables or disables the insert tab.
        /// </summary>
        INSERT,

        /// <summary>
        ///     Enables or disables the view tab.
        /// </summary>
        VIEW,

        /// <summary>
        ///     Enables or disables the design tab.
        /// </summary>
        DESIGN,

        /// <summary>
        ///     Enables or disables the brainstorming tab.
        /// </summary>
        BRAINSTORMING,

        /// <summary>
        ///     Enables or disables the organization tab.
        /// </summary>
        Organization
    }
}